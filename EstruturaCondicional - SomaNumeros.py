# Leitura de dois números
numero1 = float(input("Digite o primeiro número: "))
numero2 = float(input("Digite o segundo número: "))

# Soma dos números
soma = numero1 + numero2

# Verifica se a soma é maior que 15 e apresenta o resultado, se for o caso
if soma > 15:
    print(f"A soma dos números é {soma}, que é maior que 15.")